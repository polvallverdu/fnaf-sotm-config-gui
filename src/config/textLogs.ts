export function getObtainedTextLogs(rawTextLogs: any) {
  const actualData = rawTextLogs[0].structs;
  const obtainedTextLogs: {
    id: TextLogId;
    isUnread: boolean;
  }[] = [];

  for (const item of actualData) {
    const name = item.CustomStruct.NoteData[0].value.split("/");
    const isUnread = item.CustomStruct.bIsUnread[0].value;

    obtainedTextLogs.push({
      id: name[name.length - 1] as TextLogId,
      isUnread,
    });
  }

  return obtainedTextLogs;
}
